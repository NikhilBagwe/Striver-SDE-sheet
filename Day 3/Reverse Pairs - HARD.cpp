int main()
{
    vector<int> a {1, 3, 2, 3, 1};
    int n = a.size();
    int count = 0;
    
    for(int i=0; i<n; i++){
        for(int j=i+1; j<n; j++){
            if(a[i] > 2*a[j]) count++;
        }
    }
    
    cout<<count;

    return 0;
}

// OPTIMAL ============== Modified Merge Sort ============= TC = O(nlogn) + O(n) + O(n) ============================================================================

